
// Variavel de início / inicializção
// Condição que dita onde termina
// Aumento do o contador, ou incrementar
// cada uma dessas etapas é separada por ;

//  (inicializa        condição        incremento)
for (let contador = 0; contador <= 10; contador++) {
       console.log(contador);
}

let contador = 0


while (contador <= 10) {
    console.log(contador);
    contador++
}